    public static readonly ImageStyle[] All = [.. RealisticTheme, .. StylizedTheme];

    public static ImageStyle Random(Random rng) =>
        PromptUsedStore.Pick("style", All, s => s.Name, rng, 1)[0];

    public static ImageStyle RandomForRender(Random rng, ImageStyle render)
    {
        var list = ImageRenderCatalog.IsRealistic(render) ? RealisticTheme : StylizedTheme;
        return PromptUsedStore.Pick("style", list, s => s.Name, rng, 1)[0];
